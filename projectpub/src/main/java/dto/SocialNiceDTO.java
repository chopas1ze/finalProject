package dto;

public class SocialNiceDTO {

	private int s_nice;
	private String id;
	private int s_num;
	private int sn_num;

	public SocialNiceDTO() {
	}

	public int getS_nice() {
		return s_nice;
	}

	public void setS_nice(int s_nice) {
		this.s_nice = s_nice;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getS_num() {
		return s_num;
	}

	public void setS_num(int s_num) {
		this.s_num = s_num;
	}

	public int getSn_num() {
		return sn_num;
	}

	public void setSn_num(int sn_num) {
		this.sn_num = sn_num;
	}

}
